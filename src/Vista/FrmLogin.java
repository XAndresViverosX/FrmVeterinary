    private void BtnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnLoginActionPerformed
        db.RecognizeDATABASE("root", "", "Spa_MascotasAV");
        db.VerificarUsuarios();
        if (db.existe1 == 1) {
            db.RecognizeDATABASE("root", "", "");
        }
        if (db.existe1 == 0) {
            db.createTableRegistro("Usuarios");
        }
        Login Login=new Login();
        Login.RecognizeDATABASE("root", "", "Spa_MascotasAV");
        String ID = txtUsuario.getText();
        String Password = String.valueOf(txtPassword.getPassword());
        try {
            Login.login(ID, Password);
            if (Login.msm.equals("No Encontrado")) {
                Contador--;
                ImageIcon icono = new ImageIcon(getClass().getResource("/img/Error1.png"));
                ImageIcon mitad_0 = new ImageIcon(icono.getImage().getScaledInstance(Ancho, Alto, Image.SCALE_DEFAULT));
                JOptionPane.showMessageDialog(rootPane, "Error vuelva a intentar\nIntentos restantes: [" + Contador + "]", "ERROR", JOptionPane.PLAIN_MESSAGE, mitad_0);
                txtUsuario.setText("");
                txtPassword.setText("");
                txtUsuario.setEnabled(true);
                txtPassword.setEnabled(true);
                BtnSeguir.setEnabled(false);
                if (Contador == 0) {
                    Contador = 3;
                    txtUsuario.setEnabled(false);
                    txtPassword.setEnabled(false);
                    BtnLogin.setEnabled(false);
                    BtnSeguir.setEnabled(false);
                    lblImagen.setEnabled(false);
                    JOptionPane.showMessageDialog(rootPane, "Error vuelva a intentar mas tarde!!", "ERROR", JOptionPane.PLAIN_MESSAGE, mitad_0);
                }
            } else {
                if(ID.equals("1001")){
                ImageIcon mimagen = new ImageIcon(getClass().getResource("/img/VIVEROS-CECEP.jpg"));
                ImageIcon mitad_1 = new ImageIcon(mimagen.getImage().getScaledInstance(100, 100, Image.SCALE_DEFAULT));
                lblImagen.setIcon(mitad_1);
                BtnSeguir.setEnabled(true);
                BtnLogin.setEnabled(false);
                txtUsuario.setEditable(false);
                txtPassword.setEditable(false);
                usuario = Login.rs.getString("ROL");
                }else{
                ImageIcon mimagen = new ImageIcon(getClass().getResource("/img/root.png"));
                ImageIcon mitad_1 = new ImageIcon(mimagen.getImage().getScaledInstance(100, 100, Image.SCALE_DEFAULT));
                lblImagen.setIcon(mitad_1);
                BtnSeguir.setEnabled(true);
                BtnLogin.setEnabled(false);
                txtUsuario.setEditable(false);
                txtPassword.setEditable(false);
                usuario = Login.rs.getString("ROL");
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(FrmLogin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_BtnLoginActionPerformed

