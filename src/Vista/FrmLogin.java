    private void BtnSeguirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSeguirActionPerformed
        ImageIcon icono = new ImageIcon(getClass().getResource("/img/Question.png"));
        ImageIcon mitad_0 = new ImageIcon(icono.getImage().getScaledInstance(Ancho, Alto, Image.SCALE_DEFAULT));
        int Q = JOptionPane.showConfirmDialog(rootPane, "Deseas ir al siguiente formulario?", "MENSAJE", JOptionPane.YES_NO_OPTION, JOptionPane.PLAIN_MESSAGE, mitad_0);
        if (Q == JOptionPane.YES_OPTION) {
            BtnSeguir.setEnabled(false);

            escuchar = new ActionListener() {

                @Override
                public void actionPerformed(ActionEvent ae) {
                    cont = cont + 1;
                    Prgb.setValue(cont);
                    lblPor.setText(String.valueOf(cont) + " %");
                    lblPor.setHorizontalAlignment(0);

                    //***************************
                    if (Prgb.getValue() >= 0 && Prgb.getValue() <= 22) {
                        lblPor.setText("Verificando...");
                    }
                    //*******************
                    if (Prgb.getValue() >= 22 && Prgb.getValue() <= 36) {
                        lblPor.setText("Cargando Formulario Principal");
                    }
                    //*******************
                    if (Prgb.getValue() >= 36 && Prgb.getValue() <= 49) {
                        lblPor.setText("Accediendo por la sesion: " + usuario);
                    }
                    //*******************
                    if (Prgb.getValue() >= 49 && Prgb.getValue() <= 67) {
                        lblPor.setText("Verificando la conexion a la BD");
                    }
                    //*******************
                    if (Prgb.getValue() >= 67 && Prgb.getValue() <= 89) {
                        lblPor.setText("Analizando la integridad de los datos");
                    }
                    //*******************
                    if (Prgb.getValue() >= 89 && Prgb.getValue() <= 99) {
                        lblPor.setText("Todo esta listo :)");
                    }
                    //*******************
                    if (Prgb.getValue() == Prgb.getMaximum()) {
                        if (cont == 100) {
                            tiempo.stop();
                            FrmPrincipal Menu = new FrmPrincipal();
                            Menu.setVisible(true);
                            setVisible(false);

                        }
                    }
                }
            };

            tiempo = new Timer(110, escuchar);
            tiempo.start();//vuelve a iniciar el timer cuando finaliza la barra de progreso
            //*******************
        }
    }//GEN-LAST:event_BtnSeguirActionPerformed

